package com.rhino.jnitest.jni.utils.data;

/**
 * @since Created by LuoLin on 2018/1/12.
 **/
public class AppParam {

    /**
     * The debug flag.
     */
    public boolean mDebug;
    /**
     * The primary id of app.
     */
    public int mAppId;
    /**
     * The package name id of app.
     */
    public String mAppPackageName;
    /**
     * The version of app.
     */
    public String mAppVersion;
    /**
     * The external directory of app to store some visible file.
     */
    public String mAppExternalStoreDir;
    /**
     * The private directory of app to store some invisible file.
     */
    public String mAppPrivateStoreDir;

}
